function greatestProductOfPairs(array){
  var greatest = -Infinity;
  if(array.length % 2 !== 0) throw "Array must have an even length.";
  for(var i = 0; i < array.length; i+=2){
    greatest = array[i] * array[i+1] > greatest ? array[i] * array[i+1] : greatest;
  }
  return greatest;
}

module.exports = greatestProductOfPairs;
